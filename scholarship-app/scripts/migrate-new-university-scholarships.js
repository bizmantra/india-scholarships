const insertStmt = db.prepare(`
    INSERT OR REPLACE INTO scholarships (
        id, title, slug, provider, provider_type, state, level, caste, gender, course_stream,
        app_type, amount_annual, amount_min, amount_description, benefits, income_limit,
        min_marks, age_limit, residency_requirement, docs_needed, application_mode,
        apply_url, deadline, deadline_description, time_min, step_guide, selection,
        total_awards, renewal, competitiveness, verified_status, last_verified,
        official_source, helpline, intro_seo, faq_json, scholarship_type, status,
        verification_year, priority_score, tags
    ) VALUES (
        @id, @title, @slug, @provider, @provider_type, @state, @level, @caste, @gender, @course_stream,
        @app_type, @amount_annual, @amount_min, @amount_description, @benefits, @income_limit,
        @min_marks, @age_limit, @residency_requirement, @docs_needed, @application_mode,
        @apply_url, @deadline, @deadline_description, @time_min, @step_guide, @selection,
        @total_awards, @renewal, @competitiveness, @verified_status, @last_verified,
        @official_source, @helpline, @intro_seo, @faq_json, @scholarship_type, @status,
        @verification_year, @priority_score, @tags
    )
`);

const insertTransaction = db.transaction((scholarships) => {
    let count = 0;
    for (const s of scholarships) {
        insertStmt.run(s);
        count++;
    }
    return count;
});

